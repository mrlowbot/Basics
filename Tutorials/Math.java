package Tutorials;

/*
 * Different ways of using math commands.
 * @Karwan Maryame Azar
 */

public class Math {

	public static void main(String[] args) {

		// This just takes the minimum number and prints it.
		System.out.println(java.lang.Math.min(8.6, 5.2));

		// 5 * 5 * 5.
		System.out.println(java.lang.Math.pow(5, 3));

		// Square root.
		System.out.println(java.lang.Math.sqrt(3));

	}

}
